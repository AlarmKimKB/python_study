##################################################

# print("\n" + "2.1. 리스트 중복 제거")
l = [1,1,1,1,2,2,2,3,3,4,5,6,4,4,5,7,7,8,9,7,7,7,5,6,7]
print(l)

s = set(l)      # 리스트를 세트 자료형으로 변경
print(s)        # 중복 제거

l = list(s)     # 세트를 리스트 자료형으로 재변경
print(l)
